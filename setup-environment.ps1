if (-not $SkipDependencyInstall) {
    Write-Step 'Installing project dependencies'
    Install-Dependencies -Path $backendPath
    Install-Dependencies -Path $adminPath
    Install-Dependencies -Path $chatInterfacePath
    Install-Dependencies -Path $chatWidgetPath
}
else {
    Write-Step 'Skipping dependency installation (-SkipDependencyInstall)'
}

Write-Step 'Checking Ollama availability'
if (Test-CommandExists -CommandName 'ollama') {
    Write-Host 'Ollama command found.' -ForegroundColor Green

    if ($PullOllamaModels) {
        Write-Host "Pulling Ollama chat model: $OllamaChatModel" -ForegroundColor Yellow
        ollama pull $OllamaChatModel

        Write-Host "Pulling Ollama embedding model: $OllamaEmbeddingModel" -ForegroundColor Yellow
        ollama pull $OllamaEmbeddingModel
    }
    else {
        Write-Host 'Skipping model pull. Use -PullOllamaModels to pull required models now.' -ForegroundColor Yellow
    }
}
else {
    Write-Host 'Ollama command not found. Install from https://ollama.com/download' -ForegroundColor Yellow
    Write-Host "After install, run: ollama pull $OllamaChatModel" -ForegroundColor Gray
    Write-Host "Then run: ollama pull $OllamaEmbeddingModel" -ForegroundColor Gray
