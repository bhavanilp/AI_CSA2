Write-Step 'Validating required tools'
Assert-CommandExists -CommandName 'node' -InstallHint 'Install Node.js 18+ from https://nodejs.org/'
Assert-CommandExists -CommandName 'npm' -InstallHint 'npm ships with Node.js. Reinstall Node.js if needed.'

$nodeVersion = Get-NodeMajorVersion
if ($nodeVersion.Major -lt 18) {
    throw "Node.js 18+ is required. Detected $($nodeVersion.Raw)."
}
Write-Host "Node.js version detected: $($nodeVersion.Raw)" -ForegroundColor Green

