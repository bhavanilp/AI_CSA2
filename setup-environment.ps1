param(
    [switch]$SkipDependencyInstall,
    [switch]$PullOllamaModels,
    [switch]$ForceEnv,
    [string]$OllamaChatModel = 'qwen3:latest',
    [string]$OllamaEmbeddingModel = 'nomic-embed-text:latest'
)

Set-StrictMode -Version Latest
$ErrorActionPreference = 'Stop'

function Write-Step {
    param(
        [string]$Message,
        [ConsoleColor]$Color = [ConsoleColor]::Cyan
    )

    Write-Host "`n==> $Message" -ForegroundColor $Color
}

function Test-CommandExists {
    param([string]$CommandName)

    return $null -ne (Get-Command $CommandName -ErrorAction SilentlyContinue)
}

function Assert-CommandExists {
    param(
        [string]$CommandName,
        [string]$InstallHint
    )

    if (-not (Test-CommandExists -CommandName $CommandName)) {
        throw "Required command '$CommandName' is not available. $InstallHint"
    }
}

function Get-NodeMajorVersion {
    $raw = (& node --version).Trim()
    if (-not $raw) {
        throw 'Could not determine Node.js version.'
    }

    $normalized = $raw.TrimStart('v')
    $major = ($normalized.Split('.')[0]) -as [int]
    if ($null -eq $major) {
        throw "Unexpected Node.js version format: $raw"
    }

    return @{ Raw = $raw; Major = $major }
}

function Install-Dependencies {
    param([string]$Path)

    if (-not (Test-Path $Path)) {
        throw "Project path not found: $Path"
    }

    Write-Host "Installing npm packages in $Path" -ForegroundColor Yellow
    Push-Location $Path
    try {
        npm install
    }
    finally {
        Pop-Location
    }
}

$root = Split-Path -Parent $MyInvocation.MyCommand.Path
$backendPath = Join-Path $root 'backend'
$adminPath = Join-Path $root 'frontend\admin-dashboard'
$chatInterfacePath = Join-Path $root 'frontend\chat-interface'
$chatWidgetPath = Join-Path $root 'frontend\chat-widget'
